#include "pch.h"
#include "Channel.h"


//=============================================================================
Channel::Channel(float onset, float duration)
{
	this->onset = onset;
	this->duration = duration;
}

//=============================================================================
void Channel::setOnset(float p)
{
	onset = p;
}

//=============================================================================
float Channel::getOnset()
{
	return onset;
}


//=============================================================================
void Channel::setDuration(float p)
{
	duration = p;
}

//=============================================================================
float Channel::getDuration()
{
	return duration;
}


